#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	res;
	int	segno;

	i = 0;
	res = 0;
	segno = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		segno = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		res *= 10;
		res += str[i] - '0';
		i++;
	}
	return (res * segno);
}
