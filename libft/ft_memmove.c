#include "libft.h"

void
	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*udst;
	unsigned char	*usrc;
	size_t			i;

	udst = (unsigned char *)dst;
	usrc = (unsigned char *)src;
	i = -1;
	if (usrc < udst)
		while (len-- > 0)
			udst[len] = usrc[len];
	else
		while (++i < len)
			udst[i] = usrc[i];
	return (udst);
}
