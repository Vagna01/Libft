#include "libft.h"

int	ft_isalnum(int c)
{
	if (c <= 57 && c >= 48)
	{
		return (1);
	}
	if ((c < 91 && c > 64) || (c < 123 && c > 96))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
